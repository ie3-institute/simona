package edu.ie3.simona.test.common.exceptions

/** Indicates an error with test data
  *
  * @param message
  *   Error message to provide to the end user
  */
final case class InvalidTestDataException(message: String)
    extends Exception(message) {

  def this(message: String, cause: Throwable) = {
    this(message)
    initCause(cause)
  }

  def this(cause: Throwable) = {
    this(Option(cause).map(_.toString).orNull, cause)
  }

  def this() = {
    this(null: String)
  }

}
